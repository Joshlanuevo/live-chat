        return getBetTypeName(betType).contains("角球")
    }

    fun 是否是冠军(betType: Int): Boolean {
        return betType == 10
    }

    fun 是否是半场全场(betType: Int): Boolean {
        return betType == 16 || getBetTypeName(betType).contains("全场") || getBetTypeName(betType).contains("半场")
    }

    //由于是计算最大可赢额，所以马来和印尼按照香港盘计算即可
    fun 是否按照香港盘计算(pankouType: Int, betType: Int): Boolean {
        return if (pankouType == 1 || pankouType == 2 || pankouType == 4) {
            val typeName = getBetTypeName(betType)
            typeName.contains("让球") || typeName.contains("大小") || typeName.contains("单双")
        } else {
            false
        }
    }

    private fun getPanKouTypeName(typeId: Int): String {
        //欧洲盘—赔率包含本金（本金*赔率-本金=纯盈利）
        //香港盘—部分玩法赔率不包含本金（让球，大小，单双；本金*赔率=纯盈利）
        //马来盘—正水盘与香港盘一样计算。负水盘输少赢本金
        //印尼盘—正水盘与香港盘一样计算。负水盘输多赢本金
        //总结：正水盘是赢赔率输本金，而负水盘是赢本金输赔率
        return when (typeId) {
            1 -> "Malay Odds – 马来盘"
            2 -> "Hong Kong Odds – 香港盘"
            3 -> "Decimal Odds – 欧洲盘"
            4 -> "Indo Odds – 印度尼西亚盘"
            5 -> "American Odds – 美国盘"
            else -> "Decimal Odds – 欧洲盘"
        }
    }

