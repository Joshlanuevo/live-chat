    fun 是否是单双(betType: Int): Boolean {
        return getBetTypeName(betType).contains("单双")
    }

    fun 是否是波胆(betType: Int): Boolean {
        return getBetTypeName(betType).contains("波胆") && betType != 416 && betType != 152
    }

