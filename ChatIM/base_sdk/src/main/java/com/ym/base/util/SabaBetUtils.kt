    fun 是否是让球(betType: Int): Boolean {
        return getBetTypeName(betType).contains("让球")
    }

    fun 是否是大小球(betType: Int): Boolean {
        return getBetTypeName(betType).contains("大小")
    }

    fun 是否是独赢(betType: Int): Boolean {
        return getBetTypeName(betType).contains("独赢")
    }

    fun 是否是1x2(betType: Int): Boolean {
